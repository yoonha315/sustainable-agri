
CREATE TABLE Global_UHI_population (
    ISOURBID CHAR(7),
    ISO3 CHAR(3),
    URBID INTEGER,
    NAME CHAR(50),
    SCHNM CHAR(50),
    LATITUDE REAL,
    LONGITUDE REAL,
    ES90POP INTEGER,
    ES95POP INTEGER,
    ES00POP INTEGER,
    PRIMARY KEY (ISOURBID));

CREATE TABLE Global_UHI_UHI (
    ISOURBID CHAR(7),
    ISO3 CHAR(3),
    URBID INTEGER,
    NAME CHAR(50),
    SCHNM CHAR(50),
    LATITUDE REAL,
    LONGITUDE REAL,
    SQKM_FINAL REAL,
    URB_D_MEAN REAL,
    BUF_D_MEAN REAL,
    D_T_DIFF REAL,
    URB_N_MEAN REAL,
    BUF_N_MEAN REAL,
    N_T_DIFF REAL,
    PRIMARY KEY (ISOURBID));

CREATE TABLE USA_population (
    Geographic_area CHAR(50),
    Census Real,
    Estimated_base Real,
    Year Real,
    Population Real,
    PRIMARY KEY (Geographic_area, Year));

CREATE TABLE USA_park (
    State CHAR(20),
    Overall_Rank INTEGER,
    Green_Energy_Rank INTEGER,
    Open_Spaces_Beauty INTEGER,
    Waste_Diversion_Recycling INTEGER,
    Racial_Justice_Access_to_Clean_Outdoors INTEGER,
    PRIMARY KEY (State));

CREATE TABLE GHI_POP (
    ISOURBID CHAR(7),
    Geographic_area CHAR(50),
    Year Real,
    PRIMARY KEY (ISOURBID, Geographic_area, Year), 
    FOREIGN KEY (ISOURBID) REFERENCES Global_UHI_UHI
    ON UPDATE CASCADE,
    FOREIGN KEY (Geographic_area, Year) REFERENCES USA_population
    ON UPDATE CASCADE);

CREATE TABLE GHI_PARK (
    ISOURBID CHAR(7),
    State CHAR(20),
    PRIMARY KEY (ISOURBID, State), 
    FOREIGN KEY (ISOURBID) REFERENCES Global_UHI_UHI
    ON UPDATE CASCADE,
    FOREIGN KEY (State) REFERENCES USA_park
    ON UPDATE CASCADE);

CREATE TABLE POP_PARK (
    Geographic_area CHAR(50),
    State CHAR(20),
    Year Real,
    PRIMARY KEY (Geographic_area, Year, State), 
    FOREIGN KEY (Geographic_area, Year) REFERENCES USA_population
    ON UPDATE CASCADE,
    FOREIGN KEY (State) REFERENCES USA_park
    ON UPDATE CASCADE);

